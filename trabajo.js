// Declaración de una variable
let mensaje = "¡Hola, mundo!";

// Función para mostrar el mensaje en la consola
function mostrarMensaje() {
    console.log(mensaje);
}

// Estructura de control: condicional
if (mensaje) {
    mostrarMensaje();
} else {
    console.log("No hay mensaje para mostrar.");
}   